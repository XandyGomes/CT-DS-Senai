function somar(num1, num2){
    var res = num1 + num2
    return res
}

function sub(num1, num2){
    var res = num1 - num2
    return res
}


function div(num1, num2){
    var res = num1 / num2
    return res
}

function mult(num1, num2){
    var res = num1 * num2
    return res
}



console.log(somar(2,2))
console.log(sub(2, 2))
console.log(div(2, 2))
console.log(mult(2, 2))